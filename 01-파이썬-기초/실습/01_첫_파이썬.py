"""1장 1~3절 실습: print, 변수, 자료형, 연산자

Colab이나 본인 컴퓨터에서 한 줄씩 실행해 보세요.
한꺼번에 다 돌리기보다, 각 줄이 왜 이런 결과를 내는지 생각하며 보는 편이 훨씬 좋습니다.
"""

# 이제 첫 번째 구역이 시작된다는 사실을 눈에 띄게 보여 줍니다.
print("=" * 50)
# 지금부터 다룰 주제가 print와 변수라는 점을 제목으로 출력합니다.
print("1. print와 변수")
# 구역의 끝도 같은 선으로 닫아 주면 화면이 읽기 쉬워집니다.
print("=" * 50)

# 가장 기본적인 출력부터 시작해서 print가 화면에 글자를 보여 준다는 사실을 익힙니다.
print("Hello, World!")

# print 안에 여러 값을 넣으면 공백으로 구분되어 한 줄에 함께 출력된다는 점을 확인합니다.
print("이름:", "철수", "나이:", 25)

# 문자열 값을 name이라는 변수에 담아 두고 나중에 다시 꺼내 쓰겠습니다.
name = "철수"
# 정수 값을 age라는 변수에 담아서 숫자 데이터도 같은 방식으로 관리합니다.
age = 25
# 소수점이 있는 값도 변수에 담을 수 있다는 점을 보여 주기 위해 키를 실수로 저장합니다.
height = 175.5

# 변수에 담긴 값을 한 번에 출력하면 저장된 내용이 그대로 화면에 나타납니다.
print(name, age, height)

# 같은 변수 이름에 새 값을 넣으면 이전 값 대신 새 값이 연결됩니다.
name = "영희"
# 그래서 name을 다시 출력하면 이제는 영희가 보이게 됩니다.
print("이제 name은:", name)

# 다음 구역으로 넘어가기 전에 줄을 한 번 비워서 화면을 정리합니다.
print("\n" + "=" * 50)
# 지금부터는 값의 종류인 자료형을 살펴본다는 제목을 출력합니다.
print("2. 자료형")
# 역시 같은 길이의 구분선을 써서 화면 구성을 유지합니다.
print("=" * 50)

# a에는 소수점 없는 정수를 넣어서 int가 무엇인지 확인합니다.
a = 25
# b에는 소수점이 있는 값을 넣어서 float를 확인합니다.
b = 3.14
# c에는 글자를 넣어서 str이 문자열 자료형임을 보여 줍니다.
c = "hello"
# d에는 참이라는 의미의 True를 넣어 bool을 준비합니다.
d = True
# e에는 값이 없음을 뜻하는 None을 넣어 마지막 기본 자료형도 확인합니다.
e = None

# type 함수로 a의 자료형이 int인지 직접 확인합니다.
print(type(a))
# type 함수로 b의 자료형이 float인지도 바로 확인합니다.
print(type(b))
# 문자열은 str이라는 이름으로 표시된다는 점을 확인합니다.
print(type(c))
# 참거짓 값은 bool이라는 자료형으로 관리된다는 점을 봅니다.
print(type(d))
# None은 NoneType이라는 특별한 자료형이라는 점도 함께 확인합니다.
print(type(e))

# 숫자 25를 문자열로 바꾸면 화면에는 같아 보여도 내부 자료형은 달라집니다.
print(str(25))
# 문자로 적힌 100을 정수로 바꾸면 계산 가능한 숫자가 됩니다.
print(int("100"))
# 정수 5를 실수로 바꾸면 5.0처럼 소수점 형태가 됩니다.
print(float(5))
# 실수 3.7을 int로 바꾸면 소수점 아래가 버려진다는 점을 봅니다.
print(int(3.7))
# round를 쓰면 가장 가까운 정수로 반올림된 값이 나옵니다.
print(round(3.7))

# greeting 변수에는 인사말을 저장해서 문자열끼리 다루는 예제를 준비합니다.
greeting = "안녕"
# name 변수에는 다시 철수를 넣어 문자열 결합 예제를 자연스럽게 만듭니다.
name = "철수"

# 문자열은 더하기로 이어 붙일 수 있어서 문장을 직접 만들 수 있습니다.
print(greeting + " " + name)
# 문자열에 숫자를 곱하면 같은 문장이 여러 번 반복됩니다.
print("야호! " * 3)
# len 함수는 문자열 길이를 세어 준다는 사실도 함께 확인합니다.
print(len(greeting))

# f-string을 쓰면 문장 안에 변수 값을 자연스럽게 끼워 넣을 수 있습니다.
print(f"제 이름은 {name}이고 나이는 {age}살입니다.")
# 중괄호 안에서는 간단한 계산도 바로 할 수 있습니다.
print(f"내년 나이: {age + 1}")
# 콜론 뒤의 형식을 지정하면 소수점 자리수까지 보기 좋게 조절할 수 있습니다.
print(f"파이: {3.14159265:.2f}")

# 마지막 구역인 연산자로 넘어가기 전에 다시 화면 구분선을 출력합니다.
print("\n" + "=" * 50)
# 지금부터 사칙연산과 비교 연산을 다룬다는 제목을 보여 줍니다.
print("3. 연산자")
# 구분선을 다시 출력해서 이전 구역과 시각적으로 분리합니다.
print("=" * 50)

# 더하기는 두 수를 합친 결과를 보여 주는 가장 기본적인 산술 연산입니다.
print(10 + 3)
# 빼기는 왼쪽 수에서 오른쪽 수를 뺀 결과를 보여 줍니다.
print(10 - 3)
# 곱하기는 같은 수를 여러 번 더한 결과를 한 번에 계산합니다.
print(10 * 3)
# 나누기는 결과를 실수로 돌려주기 때문에 소수점이 보일 수 있습니다.
print(10 / 3)
# 몫 연산자는 나눈 뒤 정수 부분만 남기고 싶을 때 사용합니다.
print(10 // 3)
# 나머지 연산자는 몇 개가 남는지 확인할 때 자주 사용합니다.
print(10 % 3)
# 거듭제곱 연산자는 같은 수를 여러 번 곱하는 계산을 짧게 적게 해 줍니다.
print(2 ** 10)
# 지수를 0.5로 주면 제곱근처럼 사용할 수도 있습니다.
print(16 ** 0.5)

# 같은지 비교하면 결과가 True인지 False인지로 나옵니다.
print(5 == 5)
# 다르다는 비교도 같은 방식으로 참거짓 결과를 돌려줍니다.
print(5 != 3)
# 크다 비교는 왼쪽 값이 더 크면 True가 됩니다.
print(5 > 3)
# 작다 비교는 조건이 맞지 않으면 False가 됩니다.
print(5 < 3)

# 나이를 다시 변수에 넣고 논리 연산 예제를 준비합니다.
age = 25
# 면허 보유 여부를 True로 두고 여러 조건을 함께 판단해 보겠습니다.
has_license = True

# 성인이면서 면허도 있을 때만 운전 가능하다고 판단하도록 and를 사용합니다.
can_drive = (age >= 18) and has_license
# 계산된 참거짓 값을 출력하면 논리 연산의 결과를 바로 확인할 수 있습니다.
print("운전 가능:", can_drive)

# 학생이 아닌 상황을 만들어 or 연산과 비교해 보겠습니다.
is_student = False
# 노인 할인 대상이라고 가정해서 둘 중 하나만 참이어도 되는 예제를 만듭니다.
is_senior = True
# 학생이거나 노인이면 할인 가능하므로 or를 사용합니다.
can_discount = is_student or is_senior
# or는 둘 중 하나라도 참이면 True가 된다는 점을 결과로 확인합니다.
print("할인 가능:", can_discount)

# count를 0으로 두고 누적 증가 예제를 시작합니다.
count = 0
# += 연산자는 원래 값에 1을 더한 뒤 다시 저장하는 축약 문법입니다.
count += 1
# 같은 줄을 반복하면 값이 조금씩 누적된다는 점을 체감할 수 있습니다.
count += 1
# 한 번 더 증가시키고 최종 결과를 확인하겠습니다.
count += 1
# 최종 count 값이 3이 되었는지 출력해서 확인합니다.
print("count:", count)

# 리스트를 하나 만들어 멤버십 연산자인 in을 테스트할 준비를 합니다.
fruits = ["사과", "바나나", "딸기"]
# 사과가 리스트 안에 있으면 True가 나온다는 점을 확인합니다.
print("사과 있나?", "사과" in fruits)
# 없는 값인 포도를 찾으면 False가 나온다는 점도 함께 확인합니다.
print("포도 있나?", "포도" in fruits)

# 실습이 끝났다는 문장을 출력해서 여기까지의 구간이 마무리되었음을 알려 줍니다.
print("\n실습 끝! 결과를 한 줄씩 비교해 보세요.")
